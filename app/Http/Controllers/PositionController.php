        if($re->query('search')){
            $pos=Position::where('position_name','LIKE','%'.$re->query('search').'%')
            ->with('Department')->paginate(8);
        }
        $dep=Department::get(['id','department_name']);
        return view('teachers.positions.index',compact('pos','dep'));
    }

